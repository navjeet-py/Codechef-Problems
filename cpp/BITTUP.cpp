// Fast exponentiation from GeeksForGeeks
// URL -> https://www.geeksforgeeks.org/modular-exponentiation-power-in-modular-arithmetic/

int power(int x, int y, int p) {
    int res = 1;
    x = x % p;
    if (x == 0) return 0;
    while (y > 0) {
        if (y & 1)
            res = (res * x) % p;
        y = y >> 1;
        x = (x * x) % p;
    }
    return res;
}


